package Lessons.Lesson8;

public class isCar {

    public static void main(String[] args) {

        //shortcut to check whether boolean is true
        boolean isthisaCar = true;
        if (isthisaCar) {
            System.out.println("This is a car, vroom vroom!");
        }

        //shortcut to check whether boolean is false
        boolean isthisaJeep = false;
        if (!isthisaJeep) {
            System.out.println("This is not a jeep, it is a car.");
        }

    }
}
