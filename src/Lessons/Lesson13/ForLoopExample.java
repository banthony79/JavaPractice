package Lessons.Lesson13;

public class ForLoopExample {


    //for (int: termination: increment)
    //int i = 0; initialize a variable, this variable is just for the forloop
    //termination i < 5 (how many times you want the loop to recalculate
    //"i++ this adds a digit onto i everytime the loop recalculates
    //remember to put brackets just like an if-else statement


    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("loop " + i + " hello");
        }

        for (int i = 0; i < 10; i++) {
            System.out.println("Bryce is awesome #" + i);
        }
    }

}
