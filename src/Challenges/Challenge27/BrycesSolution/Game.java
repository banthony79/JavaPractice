package Challenges.Challenge27.BrycesSolution;

public class Game {

    private String name;
    private String storageMedia;
    private String system;

    public Game(String name, String storageMedia, String system) {
        this.name = name;
        this.storageMedia = storageMedia;
        this.system = system;
    }

    public String getName() {
        return name;
    }

    public String getStorageMedia() {
        return storageMedia;
    }

    public String getSystem() {
        return system;
    }


}
