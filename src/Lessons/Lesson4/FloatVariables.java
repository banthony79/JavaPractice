package Lessons.Lesson4;

public class FloatVariables {


    public static void main(String[] args) {

        float myMaxFloatValue = Float.MAX_VALUE;
        float myMinFloatValue = Float.MIN_VALUE;
        System.out.println("The max value for a float is " + myMaxFloatValue);
        System.out.println("The min value for a float is " + myMinFloatValue);

        double myMaxDoubleValue = Float.MAX_VALUE;
        double myMinDoubleValue = Float.MIN_VALUE;
        System.out.println("The max value for a double is " + myMaxDoubleValue);
        System.out.println("The min value for a double is " + myMinDoubleValue);

        int myIntValue = 5;
        float myFloatValue = 5.25f;
        double myDoubleValue = 5.25;


    }
}